package javaBasics;

public class Bitwiseoperators {

	public static void main(String[] args) {
		int a= 10;
		int b = 8;
		
		// bitwise and- (0,0)=0,(1,1)=1,(0,1)=0,(1,0)=0
		System.out.println("a&b="+(a&b));
		
		//bitwise xor exclusive or- different values gets 1 and same values gets 0 i.e, (1,1)=0,(1,0)=1
		System.out.print("a");

	}

}
